package Review3;

public class ForLoopExamples {
    public static void main(String[] args) {
        for(int i=10; i>5;i--){
            System.out.println(i);

        }
        for(int j=5; j<=1; j--){
            System.out.println("Happy");
        }
        for(int i= 1; i<=6; i++){    // to print even numbers

            if(i%2==0){
                System.out.println("even number"+i);
            }

        }

    }
}
